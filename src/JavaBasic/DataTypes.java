package JavaBasic;

public class DataTypes {

	public static void main(String[] args) {

		// Premitive data types int, char, double, boolean
		
		// 1 int
		int i = 20;
		
		System.out.println(i);
		i = 10; // replacing 20 to 10 in memory
		int k = -20;

		// 2. double
		double d = 12.33;
		
		
		double d1 = 32.33;
		System.out.println(d1);
		double d2 = 100; // 100.00

		// 3.Char single digit value should be written in single

		char a = 'A';
		char a1 = 'a';
		System.out.println(a1);
		char a2 = '2';

		// 4. boolean
		boolean b1 = true;
		System.out.println(b1);
		boolean b2 = false;

		// 5. String - String is a class

		String s = "Hello";
		String s1 = "world";
		System.out.println(s1);
		String s2 = "1234";

	}

}
